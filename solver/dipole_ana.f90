!		 TAILS FOR ADAPTIVE FILTERING.  A TRUNCATION CRITERION IS
!		 DEFINED DURING CONVOLUTION IN A FIXED ABCISSA RANGE AS
!		 THE MAX. ABSOLUTE CONVOLVED PRODUCT TIMES TOL. TYPICALLY,
!		 TOL.LE.0.00001 WOULD GIVE ABOUT .01 PER CENT ACCURACY
!		 FOR WELL-BEHAVED KERNELS AND MODERATE VALUES OF B.  FOR
