!# Coded on Dec. 1, 2015 by Takuto Minami
!# to calculate analytical solution based on Ward and Hohmann (1988)
!# Numerical integration is conducted by the method of Anderson (1979)
!# dipole is directed in the y-direction and located at the origin
!# you can choose y coordinate for observation line
!# The output of this
!#
!# In this program, electromagnetic filed can be calculated only at ground surface
subroutine dipole_ana(x3,b3,iflag0) ! calculate b3(1:3) [nT]
use m_param_ana ! includes iflag
implicit none
integer(4),intent(in) :: iflag0
real(8) :: x3(3) ! [km]
real(8) :: coef
complex(8),dimension(3),intent(out) :: b3 ! [nT] 1: x, 2:y, 3:z
complex(8) :: BY1,BY2,BX1,BX2
real(8) :: R, DX,DY ! R=dsqrt(x^2+y^2)
integer(4) :: j
!=================== Parameter for ZHANKS
real(8),   parameter :: TOL=0.d0 ! Tolerance for truncation
integer(4),parameter :: NEW=1 ! always new
integer(4)           :: NF ! output from ZHANKS
COMPLEX(8)           :: FX,FY0,FY1,FZ,ZH, ZHANKS
external FX, FY0,FY1,FZ
!=================== Parameter for ZHANKS

iflag=iflag0 ! 1: primary, 2: secondary, 3: total
!#[1]## set
xobs=x3(1)*1.d3 ! [km] to [m]
yobs=x3(2)*1.d3 ! [km] to [m]
zobs=x3(3)*1.d3 ! [km] to [m]

!#[2]## observation info and allocate
 coef=I*ds*mu/4./pi*1.d+9 ! [nT]

!#[3]## Bz
 R=dsqrt(xobs**2.d0+yobs**2.d0) ![m]/L0
 ZH=ZHANKS(1,R,FZ,TOL,NF,NEW) ! variables used via m_param, 1 for first order Bessel
 b3(3)=coef*yobs/R*ZH ! [nT] downward positive

!#[4]## By
 !#[4-1] J1
 DX=0.1d0 ! [m] = 10cm
 !# xobs + DX
 R=dsqrt((xobs+DX)**2.d0+yobs**2.d0)
 BY1 =   (xobs+DX)/R*ZHANKS(1,R,FY1,TOL,NF,NEW) ! variables via m_param, 1 for first order Bessel
 !# xobs - DX
 R=dsqrt((xobs-DX)**2.d0+yobs**2.d0)
 BY2  =  (xobs-DX)/R*ZHANKS(1,R,FY1,TOL,NF,NEW) ! variables via m_param, 1 for first order Bessel
 !# BY= - coeff * (BY1 -BY2)/2DX
 b3(2)= - coef*(BY1-BY2)/(2.*DX) ! [nT] downward positive
 !#[4-2] J0
 R=dsqrt(xobs**2.d0+yobs**2.d0)
 b3(2) = b3(2) - coef*ZHANKS(0,R,FY0,TOL,NF,NEW)

!#[5]## Bx
 !# J1
 DX=0.1d0 ! [m] 10cm
 !# yobs + DY
 R=dsqrt((xobs+DX)**2.d0+yobs**2.d0) ! [m]/L0
 BX1   =  yobs/R*ZHANKS(1,R,FX,TOL,NF,NEW) ! variables used via m_param, 1 for first order Bessel
 !# yobs - DY
 R=dsqrt((xobs-DX)**2.d0+yobs**2.d0)
 BX2   =  yobs/R*ZHANKS(1,R,FX,TOL,NF,NEW) ! variables used via m_param, 1 for first order Bessel
 !# BX= coeff * (BX1 -BX2)/2DX
 b3(1)= coef*(BX1-BX2)/(2.*DX) ! [nT] downward positive

return
end subroutine dipole_ana
