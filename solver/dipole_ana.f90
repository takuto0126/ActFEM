!    B     = REAL TRANSFORM ARGUMENT B.GT.0.0 IF THE HANKEL TRANSFORM.
!            IF NEW=0, B IS ASSUMED EQUAL TO THE LAST B USED WHEN NEW=1
!            (SEE PARAMETER NEW AND SUBPROGRAM USAGE BELOW).
!    FUN(G)= EXTERNAL DECLARED COMPLEX FUNCTION NAME (USER SUPPLIED)
!            OF A REAL ARGUMENT G.GT.0. THIS REFERENCE MUST BE SUPPLIED
!		 EVEN WHEN NEW=0, SINCE THE ADAPTIVE CONVOLUTION
!		 MAY NEED SOME DIRECT FUNCTION CALLS (E.G. IF TOL REDUCED).
