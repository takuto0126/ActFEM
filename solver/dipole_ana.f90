!  CALL SAVER(I,J) WILL REPLACE FSAVE() ARRAY WITH
!  FSAVE(K)=GSAVE(K)**I * FSAVE(K)**J
!  FOR K=1,NSAVE.
!
!  INPUT PARAMETERS (I,J) MAY BE NEGATIVE,ZERO, OR POSITIVE INTEGERS.
!
!--SUBROUTINE SAVER MAY BE USED IN CONJUNCTION WITH SUBPROGRAM ZHANKS
!  TO MODIFY SAVED KERNELS WHEN USING NEW=0 OPTION (SEE ZHANKS).
!
	implicit real(selected_real_kind(8))(a-h,o-z) ! added on Dec. 1, 2015
