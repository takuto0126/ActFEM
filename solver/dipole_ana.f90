!# Coded on Dec. 1, 2015
!========================================================== FX
!# see Ward and Hohmann (1988), p.233
complex(8) function FX(G0)
use m_param_ana
real(8),intent(in) :: G0
  CALL CALRTEM(G0) ! get G, G2, RTE, RTM, u0 from G0
  if (iflag .eq. 1) FX=0.d0
  if (iflag .ne. 1) FX= (RTM + RTE)*cdexp(u0*zobs)
return
end
!========================================================== FY1
complex(8) function FY1(G0)
use m_param_ana
real(8),intent(in) :: G0
  CALL CALRTEM(G0) ! get G, G2, RTE, RTM, u0 from G0
  if (iflag .eq. 1) FY1=0.d0
  if (iflag .ne. 1) FY1= (RTM + RTE)*cdexp(u0*zobs)
return
end
!========================================================== FY0
complex(8) function FY0(G0)
use m_param_ana
real(8),intent(in) :: G0
  CALL CALRTEM(G0) ! get G, G2, RTE, RTM, u0 from G0
  if (iflag .eq. 1) FY0 = 1.0 *cdexp(u0*zobs)*G
  if (iflag .eq. 2) FY0= - RTE*cdexp(u0*zobs)*G
  if (iflag .eq. 3) FY0= (1.0 - RTE)*cdexp(u0*zobs)*G
return
end
!========================================================== FZ
complex(8) function FZ(G0)
use m_param_ana
real(8),intent(in) :: G0
  CALL CALRTEM(G0) ! get G, G2, RTE, RTM, u0 from G0
  if (iflag .eq. 1)  FZ=       1.0 *cdexp(u0*zobs)*G2/u0
  if (iflag .eq. 2)  FZ=       RTE *cdexp(u0*zobs)*G2/u0
  if (iflag .eq. 3)  FZ= (1.d0+RTE)*cdexp(u0*zobs)*G2/u0
return
end
!===========================================================  RTE and RTM
subroutine CALRTEM(G0)
use m_param_ana
implicit none
real(8),intent(in)  :: G0
complex(8) :: Y_hat(0:nlayer), Y(0:nlayer),u(0:nlayer)
complex(8) :: Z_hat(0:nlayer), Z(0:nlayer), th, k2
integer(4) :: j
  G=G0 ; G2=G*G ! G is unscaled radial wave number

!#[1]## coefficients, G2, u, Y=u/i/omega/mu
 do j=0,nlayer
  k2=-iunit*mu*omega*(cond(istructure,j)+iunit*omega*epsiron)
  u(j)=cdsqrt(G2-k2)
  Y(j)=u(j)/(iunit*omega*mu)
  Z(j)=u(j)/(cond(istructure,j)+iunit*omega*epsiron) ! Z=u'/cond/L0
 end do
!#[2]## RTE
 Y_hat(nlayer)=Y(nlayer)
 Z_hat(nlayer)=Z(nlayer)
 do j=nlayer-1, 1, -1
  Y_hat(j)=Y(j)*(Y_hat(j+1)+Y(j)      *th(u(j)*h(j)))&
  &            /(Y(j)      +Y_hat(j+1)*th(u(j)*h(j)))
  Z_hat(j)=Z(j)*(Z_hat(j+1)+Z(j)      *th(u(j)*h(j)))&
  &            /(Z(j)      +Z_hat(j+1)*th(u(j)*h(j)))
 end do
 RTE=(Y(0)-Y_hat(1))/(Y(0)+Y_hat(1))
 RTM=(Z(0)-Z_hat(1))/(Z(0)+Z_hat(1))
 u0=u(0)
return
end

!########################################################### tanh
      function th(a)
      implicit real(selected_real_kind(8))(a-h,o-z)
      complex(8) :: th,a
      th=(1.d0 - cdexp(-2.*a))/(1.d0 + cdexp(-2.*a))
      return
      end
