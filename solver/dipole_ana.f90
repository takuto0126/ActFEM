!		 REAL ARGUMENTS IN GSAVE FOR THE GIVEN PARAMETER B.
!	NEW =0 TO USE RELATED KERNELS (MODIFIED BY USER) CURRENTLY STORED
!		 IN COMMON/SAVE/. FUN IS CALLED ONLY IF REQUIRED
!		 DURING THE CONVOLUTION. ADDITIONAL FUNCTION VALUES WHEN
!		 NEEDED ARE AUTOMATICALLY ADDED TO THE COMMON/SAVE/ BLOCK.
!
!   *******  NOTE THAT IT IS THE USERS RESPONSIBILITY TO MODIFY THE
